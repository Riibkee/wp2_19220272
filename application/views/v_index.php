        <li>Perubahan perilaku</li>
<p align='justify'>Ini merupakan tanda munculnya penyakit mental 
pada anak yang tergolong mudah Anda sadari melalui aktivitas sehari-hari 
baik di rumah maupun di sekolah. Ketika anak menjadi lebih sering bertengkar, 
cenderung kasar, hingga berkata kasar yang menyakitkan orang lain padahal 
sebelumnya tidak, Anda perlu curiga. Tak hanya itu saja, Anda juga mungkin 
melihat perubahan perilaku anak seperti menjadi lebih mudah marah dan merasa frustasi.</p>

<li>Perubahan mood</li>
<p align='justify'>Tanda penyakit mental lainnya adalah mood atau suasana hati anak 
yang berubah secara tiba-tiba. Kondisi ini bisa berlangsung sebentar hingga dalam jangka waktu yang tidak menentu.
Tentunya, hal ini bisa mengakibatkan masalah pada hubungan dengan keluarga serta teman sebaya. 
Ini merupakan gejala umum dari depresi, ADHD, hingga kelainan bipolar.</p>

<li>Kesulitan berkonsentrasi</li>
<p align='justify'>Anak-anak yang menderita gangguan mental cenderung sulit fokus atau memperhatikan 
dalam waktu yang lama. Selain itu, mereka juga memiliki kesulitan untuk duduk diam dan membaca. 
Tanda penyakit mental yang satu ini dapat menyebabkan menurunnya performa di sekolah juga perkembangan otaknya.</p>

<li>Penurunan berat badan</li>
<p align='justify'>Tahukah Anda bahwa gangguan mental juga dapat memengaruhi kondisi fisik anak? 
Tak hanya karena penyakit fisik, berat badan yang menurun drastis juga bisa menjadi tanda penyakit mental anak. 
Gangguan makan, stres, hingga depresi dapat menjadi penyebab anak kehilangan nafsu makan, mual, dan muntah yang berkelanjutan.</p>

<li>Perasaan yang intens</li>
<p align='justify'>Anak-anak kadang menghadapi perasaan takut yang berlebihan tanpa alasan. 
Tanda gangguan mental pada anak ini seperti menangis, berteriak atau mual disertai dengan perasaan sangat intens. 
Perasaan ini pun dapat menyebabkan efek seperti kesulitan bernapas, jantung berdebar atau bernapas dengan cepat, 
yang dapat mengganggu aktivitas sehari-hari.</p>

<li>Menyakiti diri sendiri</li>
<p align='justify'>Perhatikan saat anak sering mengalami kekhawatiran serta rasa takut berlebih. Perasaan ini dapat berujung 
pada keinginannya untuk menyakiti diri sendiri.
Biasanya, ini menjadi akumulasi dari perasaan stres serta menyalahkan diri sendiri karena gangguan mental juga mengakibatkan 
anak sulit mengelola emosi. Ini juga menjadi tanda gangguan mental pada anak yang perlu Anda cermati karena tidak menutup kemungkinan 
berujung pada percobaan bunuh diri.</p>

<li>Muncul berbagai masalah kesehatan</li>
<p align='justify'>Penyakit atau gangguan mental juga dapat ditandai dengan masalah pada kesehatannya, misal anak mengalami sakit kepala dan sakit perut yang berkelanjutan.</p></ol>
